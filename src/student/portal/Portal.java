    public int addCredits(){
     int sum=0;
        
     for(int i=0; i<jTableData.getRowCount();i++){
        sum=sum+Integer.parseInt(jTableData.getValueAt(i,1).toString());
     }
      return sum;
    }
    
    
    
    public float sumCG(){
         float x=0;
        
      for(int i=0; i<jTableData.getRowCount();i++){
        float amount=(Float.parseFloat(jTableData.getValueAt(i,1).toString()))*(Float.parseFloat(jTableData.getValueAt(i,3).toString()));
        x=x+amount;
      }
      return x;
    }
     
    
    
    public double cgpa(){
     double x= (double)sumCG()/addCredits();
     return  (Math.round(x*100.0)/100.0);
    }
    
    

    public void Probation(){
     if(cgpa()<2){jTextProbation.setText("YES");}
     else{jTextProbation.setText("NO");}
    }
    
    
    public void Rank(){
        if(cgpa()>3.0){jTextRank.setText("1st Class");}
        else if(cgpa()>2.0 && cgpa()<3.0){jTextRank.setText("2nd Class");}
        else if(cgpa()>1.0 && cgpa()<2.0){jTextRank.setText("3rd Class");}
        else if(cgpa()>0 && cgpa()<1.0){jTextRank.setText("Under Class");}
    }
    
    public void Feedback(){
        if(cgpa()>=3.8){jTextFeedback.setText("Your Result is Excellent");}
        else if(cgpa()>=3.5 && cgpa()<3.8){jTextFeedback.setText("Your Result is Very Good");}
        else if(cgpa()>=3.0 && cgpa()<3.5){jTextFeedback.setText("Your Result is Good");}
        else if(cgpa()>=2.5 && cgpa()<3.0){jTextFeedback.setText("Your Result is Average. Try hard to increase your CGPA!");}
        else if(cgpa()>=2.0 && cgpa()<2.5){jTextFeedback.setText("Your Result is Below Average");}
        else if(cgpa()<=2.0){jTextFeedback.setText("Your Result is Poor. Work Hard!");}
    }
    
