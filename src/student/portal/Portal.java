    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
        if(!jTextCourse.getText().isEmpty())
        {
          DefaultTableModel m=(DefaultTableModel) jTableData.getModel();// created table object
          m.addRow(new Object[]{jTextCourse.getText(),jComboBoxCredits.getSelectedItem(),jComboBoxGrade.getSelectedItem(),WordtoNumber()} ); //Transferring data to the table        
        }
        else
        {
          JOptionPane.showMessageDialog(null,"No Course !");
        }
        jTextCourse.setText(null);
        jComboBoxCredits.setSelectedItem(null);
        jComboBoxGrade.setSelectedItem(null);
    }//GEN-LAST:event_jbtnAddActionPerformed

